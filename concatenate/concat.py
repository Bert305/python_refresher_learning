


first_name = "Bob"

last_name = "Seals"

print(first_name + last_name) # BobSeals ---> concatenation of strings
print(first_name + " " + last_name) # Bob Seals ---> concatenation of strings with a space in between
print(f"{first_name} {last_name}") # Bob Seals ---> f-string formatting
print("My name is " + first_name + " " + last_name) # My name is Bob Seals ---> concatenation of strings with a space in between